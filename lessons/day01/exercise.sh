#!/usr/bin/env bash
# Day 01 演習: Vim移動 × Bash変数・echo
#
# 【ルール】Vimで開いてhjklで移動しながら編集すること！
# 矢印キーは使わない。
#
# 【使い方】
#   vim lessons/day01/exercise.sh  で開く
#   /YOUR  で次のタスクへジャンプ
#   :w     で保存
#   bash lessons/day01/test.sh  で採点

# ============================================================
# Task 1: 変数に自分の名前を代入して表示する
# ============================================================
# 期待する出力例: "こんにちは、Alice！"

# YOUR CODE HERE: name変数に自分の名前を代入する
name="Yusuke"

echo "こんにちは、${name}！"


# ============================================================
# Task 2: 2つの数値を変数に入れて足し算の結果を表示する
# ============================================================
# 期待する出力例: "10 + 32 = 42"

# YOUR CODE HERE: num_a に 10, num_b に 32 を代入する
num_a=10
num_b=32

result=$((num_a + num_b))
echo "${num_a} + ${num_b} = ${result}"


# ============================================================
# Task 3: 今日の日付を変数に入れて表示する
# ============================================================
# 期待する出力例: "今日は 2026-04-19 です"
# ヒント: $(date +%Y-%m-%d) で今日の日付が取れる

# YOUR CODE HERE: today変数に今日の日付を代入する
today="$(date +%Y-%m-%d)"

echo "今日は ${today} です"


# ============================================================
# Task 4: 自己紹介文を作る（複数変数の組み合わせ）
# ============================================================
# 期待する出力例: "私の名前はAliceです。好きな数は42です。"

# YOUR CODE HERE: favorite_number 変数に好きな数を代入する
favorite_number=10

echo "私の名前は${name}です。好きな数は${favorite_number}です。"
