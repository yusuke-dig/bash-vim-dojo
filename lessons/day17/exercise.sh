#!/bin/bash
# Day 17: ctags / タグジャンプ × 関数の整理・モジュール化
#
# 【Vim での使い方】
#   1. vim exercise.sh で開く
#   2. :! ctags -R .  でタグファイルを生成
#   3. 関数名にカーソルを合わせて Ctrl-] でジャンプ
#   4. :e lib.sh で lib.sh を開いて to_upper を定義（:wq で保存）
#   5. Ctrl-t または :e exercise.sh で戻る

# ================================================================
# Task 1: greet 関数を定義する
# 引数で受け取った名前に挨拶する: "Hello, [名前]!" を echo する
# ----------------------------------------------------------------
# ヒント: function greet() { echo "Hello, $1!"; } のように定義する
# YOUR CODE HERE
function greet() {
 echo "Hello, $1!"
}

# ================================================================
# Task 2: add 関数を定義する
# 2つの引数（数値）を受け取り、その合計を echo する
# ----------------------------------------------------------------
# ヒント: function add() { echo $(( $1 + $2 )); } のように定義する
# YOUR CODE HERE
function add() {
  echo $(( $1 + $2 ));
}

# ================================================================
# Task 3: lib.sh を source して to_upper 関数を読み込む
# （先に :e lib.sh を開いて to_upper 関数を定義・保存しておくこと）
# ----------------------------------------------------------------
# ヒント: source lib.sh
# YOUR CODE HERE
source lib.sh

# ================================================================
# Task 4: greet "Vim" を呼び出す
# ----------------------------------------------------------------
# ヒント: greet "Vim"
# YOUR CODE HERE
greet "Vim"

# ================================================================
# Task 5: add 7 3 と to_upper "bash" を呼び出す（2行）
# ----------------------------------------------------------------
# ヒント: add 7 3 と to_upper "bash" をそれぞれ1行ずつ呼ぶ
# YOUR CODE HERE
add 7 3
to_upper "bash"
