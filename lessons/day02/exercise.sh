#!/bin/bash
# Day 02: Vim挿入・削除 × Bashのif文
# このファイルをVimで開いて編集してください: vim exercise.sh
#
# 操作のヒント:
#   i  → カーソルの前に挿入
#   a  → カーソルの後ろに挿入
#   A  → 行末に挿入
#   x  → 1文字削除
#   dd → 行ごと削除
#   u  → undo（取り消し）

# ===========================================================
# Task 1: 変数に値をセットする
# 下の行の YOUR_NAME を自分の名前に書き換えてください
# ヒント: カーソルを "Y" に移動して "cw" (change word) か
#         "x" を連打して消してから "i" で入力
# ===========================================================
NAME="Yusuke"

# ===========================================================
# Task 2: if文を完成させる
# 下のif文には比較演算子が抜けています
# "OPERATOR" を適切な演算子（-eq, -ne, -lt, -gt など）に変えてください
# ゴール: score が 60 以上なら "合格" と表示する
# ===========================================================
score=75
if [ $score -ge 60 ]; then
  echo "合格"
else
  echo "不合格"
fi

# ===========================================================
# Task 3: elif節を追加する
# 下のif文に elif を追加して3段階評価にしてください
# ゴール:
#   90以上 → "優秀"
#   60以上89以下 → "合格"
#   59以下 → "不合格"
# ヒント: "合格" の行の下に elif 節を "A" + Enter で追加
# ===========================================================
grade=82
if [ $grade -ge 90 ]; then
  echo "優秀"
elif [ $grade -ge 60 ] && [ $grade -le 89 ]; then
 echo "合格"
else
  echo "不合格"
fi

# ===========================================================
# Task 4: 不要な行を削除する
# 下のコードには余計な echo 行があります
# "余計な行" と書かれた行を dd で削除してください
# ===========================================================
weather="sunny"
if [ "$weather" = "sunny" ]; then
  echo "晴れです"
else
  echo "晴れではありません"
fi

# ===========================================================
# Task 5: 文字列比較を追加する
# 変数 day に値をセットして、"Monday" なら "週の始まり" と
# 表示するif文を書いてください
# 下の # YOUR CODE HERE の部分を埋めてください
# ===========================================================
# YOUR CODE HERE

day="Monday"

if [ "$day" = "Monday" ]; then
  echo "週の始まり"
fi
